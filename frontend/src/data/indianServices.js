// Helper functions to filter services
export const getServicesByCity = (city) => {
  return INDIAN_SERVICES.filter(service => service.location.city === city);
};

export const getServicesByCategory = (category) => {
  return INDIAN_SERVICES.filter(service => service.category === category);
};

export const searchServices = (query) => {
  const lowercaseQuery = query.toLowerCase();
  return INDIAN_SERVICES.filter(service => 
    service.title.toLowerCase().includes(lowercaseQuery) ||
    service.description.toLowerCase().includes(lowercaseQuery) ||
    service.category.toLowerCase().includes(lowercaseQuery) ||
    service.location.city.toLowerCase().includes(lowercaseQuery)
  );
};

export const getServiceById = (id) => {
  return INDIAN_SERVICES.find(service => service._id === id);
};

export default INDIAN_SERVICES;
